<h3>SNAP Project Design</h3>
<p>
Although we offer a wealth of model outputs and other data, SNAP is by no means simply a data portal.  Without proper interpretation, climate projections can be misleading and potentially misused.  Effective communication and meaningful planning for the future are central to our mission.  SNAP makes data and resources publicly available with the understanding that all use of our resources will be properly <attributed> and that every effort will be made to appropriately explain and interpret SNAP products by making methods and model uncertainty as transparent as possible.
</p>
<p>
Every SNAP project is a collaborative effort.  Some collaborators use data and information directly from this website to create their own tools for education, research, and planning.  Other <a href="collaborators.php">collaborators</a> work with SNAP researchers on complex multi-year <a href="projects.php">projects</a>, using external or joint funding. At the core of every project is the goal of creating products that are both pertinent and understandable to their intended users.
</p>

<h4>Planning Methods</h4>
<p>
Depending on the needs and interests of SNAP&rsquo;s project partners, SNAP offers data and analysis expressed in either standard scientific language (e.g. ranges of temperature projections with error values representing the standard deviation between models) or in more qualitative forms such as narrative scenarios.
</p>
<p>
Planning for multiple potential scenarios offers one way deal with uncertainty.  Scenario-building has been used successfully for more than thirty years by businesses and corporations.  This method asks planners to explore challenging &ldquo;what if&ldquo; questions by using models and other available information  to describe several possible futures.  Scenarios should be plausible&mdash;that is, within the range of reasonable possibility&mdash;but they should also be different from one another, thus challenging leaders and managers to come up with strategies that are flexible enough to address each possibility.
</p>

<p>Peter Schwartz of Global Business Network offers a <a href="http://www.gbn.com/" target="_blank">comprehensive primer</a> on scenario-building and its benefits in The Art of the Long View (1991).
</p>
